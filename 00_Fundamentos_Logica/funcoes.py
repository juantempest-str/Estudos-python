def gerar_relatorio_corrida(distancia, tempo):
    print("\n=== RELATÓRIO DE DESEMPENHO ===")
    print(f"Distância percorrida: {distancia} km")
    print(f"Tempo total: {tempo} minutos")
    print("================================\n")
print("Iniciando o sistema de treinos...")
gerar_relatorio_corrida(2.85, 25)
gerar_relatorio_corrida(5.0, 45)